package selectStatements;

public class ForAndWhileLoop {

	public static void main(String[] args) {
		// for(initialization;condition;increment)

		for (int i = 1; i <= 10; i++) {

			System.out.println(i);

		}
		// to print the odd numbers
		for (int i = 1; i <= 10; i++) {

			System.out.println(i);
			i++;
		}
		
		int j = 1;
		while (j <= 10) {
			System.out.println(j);
			j++;
		}
		
		
		for (int i = 1; i <= 10; i++) {

			System.out.println(i);
			

		}
		
	}
}